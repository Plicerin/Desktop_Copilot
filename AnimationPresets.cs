    public static string GetDisplayName(BuiltInAnimationPreset preset)
    {
        return preset switch
        {
            BuiltInAnimationPreset.BalletSilhouette => "Ballet Silhouette",
            BuiltInAnimationPreset.CalmBlink => "Calm Blink",
