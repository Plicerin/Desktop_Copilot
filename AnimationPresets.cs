    private static AnimationFrame Frame(int milliseconds, params string[] lines)
    {
        return new AnimationFrame(
            string.Join(Environment.NewLine, lines),
            TimeSpan.FromMilliseconds(milliseconds));
    }

    private static AnimationFrame FrameBlock(int milliseconds, string content)
    {
        return new AnimationFrame(
            content.Replace("\r\n", "\n", StringComparison.Ordinal).Trim('\n', '\r'),
            TimeSpan.FromMilliseconds(milliseconds));
    }
